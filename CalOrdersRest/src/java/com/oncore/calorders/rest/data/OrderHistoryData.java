package com.oncore.calorders.rest.data;

import java.util.Date;

/**
 *
 * @author oncore
 */
public class OrderHistoryData {

    private Date orderDate;
    private String orderStatus;
    private String orderPoNumber;
    private String orderAgency;
    private String orderPrice;
    private String orderDescription;

    public Date getOrderDate() {
        return orderDate;
    }

    public void setOrderDate(Date orderDate) {
        this.orderDate = orderDate;
    }

    public String getOrderStatus() {
        return orderStatus;
    }

    public void setOrderStatus(String orderStatus) {
        this.orderStatus = orderStatus;
    }

    public String getOrderPoNumber() {
        return orderPoNumber;
    }

    public void setOrderPoNumber(String orderPoNumber) {
        this.orderPoNumber = orderPoNumber;
    }

    public String getOrderAgency() {
        return orderAgency;
    }

    public void setOrderAgency(String orderAgency) {
        this.orderAgency = orderAgency;
    }

    public String getOrderPrice() {
        return orderPrice;
    }

    public void setOrderPrice(String orderPrice) {
        this.orderPrice = orderPrice;
    }

    public String getOrderDescription() {
        return orderDescription;
    }

    public void setOrderDescription(String orderDescription) {
        this.orderDescription = orderDescription;
    }
}
